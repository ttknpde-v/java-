   @Override
   public void actionPerformed(ActionEvent e) {
      // focus conditions we use e for accessing JButton
      Toy toy = myActionPerformed(e);
      if (toy != null) {
         update(toy);
      }
   }

   private Toy myActionPerformed(ActionEvent actionEvent) {
      if (actionEvent.getActionCommand().equals("Search")) {
         // for showing info that use search
         // connect and query
         toy = toyService.read(Long.valueOf(jTextFieldToyId.getText())); // if not fould it will throw error
         // set toy to each field
         jTextFieldProductName.setText(toy.getProductName());
         jTextFieldProductPrice.setText(toy.getProductPrice().toString());
         String status = toy.getProductStatus().toString();
         
         if (status.equals("use")) {
            jRadioButtonUse.setSelected(true);
         }
         if (status.equals("unused")) {
            jRadioButtonUnused.setSelected(true);
         }
         

      } else if (actionEvent.getActionCommand().equals("Update")) {
         
         toy = new Toy();
         toy.setToyId(Long.valueOf(jTextFieldToyId.getText()));
         toy.setProductName(jTextFieldProductName.getText());
         toy.setProductPrice(Float.valueOf(jTextFieldProductPrice.getText()));
         if (jRadioButtonUnused.isSelected()) {
            toy.setProductStatus(Status.valueOf(jRadioButtonUnused.getText()));
         } else if (jRadioButtonUse.isSelected()) {
            toy.setProductStatus(Status.valueOf(jRadioButtonUse.getText()));
         }
         // then return
         return toy;
      }
      return null;
   }

   private void update(Toy toy) {
      ImageIcon icon;
      Image image;
      Image newimg;
      
      Integer result = toyService.update(toy); // connect and query 
      
      if (result > 0 && result != null) { // used than check
         
         icon = new ImageIcon("src/resources/images/checked.png");
         image = icon.getImage(); // transform it 
         newimg = image.getScaledInstance(60, 60, java.awt.Image.SCALE_SMOOTH); // scale it the smooth way  
         icon = new ImageIcon(newimg); // transform it back
         JOptionPane.showMessageDialog(this, "updated", "warning", JOptionPane.INFORMATION_MESSAGE, icon);
      
      } else {
         
         icon = new ImageIcon("src/resources/images/remove.png");
         image = icon.getImage(); // transform it 
         newimg = image.getScaledInstance(60, 60, java.awt.Image.SCALE_SMOOTH); // scale it the smooth way  
         icon = new ImageIcon(newimg); // transform it back
         JOptionPane.showMessageDialog(this, "failed", "warning", JOptionPane.INFORMATION_MESSAGE, icon);
      
      }
      
      // clear value each fileds 
      jTextFieldProductName.setText("");
      jTextFieldProductPrice.setText("");
      jTextFieldToyId.setText("");
      buttonGroupStatus.clearSelection(); // clear all jradio button
   }

